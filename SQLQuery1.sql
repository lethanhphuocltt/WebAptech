CREATE TABLE MAKE
(
	IDMAKE INT IDENTITY(1,1) PRIMARY KEY,
	BRANDS NVARCHAR(30),
	ADDRESSBRANDS NVARCHAR(100)
)

--
CREATE TABLE WARRANTY
(
	IDWARRANTY INT IDENTITY(1,1) PRIMARY KEY,
	TIMEWARRANTY VARCHAR(30),
	SCHEMES NVARCHAR(200)
)
--
CREATE TABLE CAR 
(
	IDCAR INT IDENTITY(1,1) PRIMARY KEY,
	NOTE VARCHAR (300),
	PRICE FLOAT,
	NAMECAR VARCHAR(100),
	FUELTYPE VARCHAR(30),
	STOCK VARCHAR(30),
	GASOLINE VARCHAR(30),
	ENGINE VARCHAR (30),
	TRANSMISSION VARCHAR(30),
	VIN VARCHAR(30),
	MILEAGE VARCHAR(30),
	IDMAKE INT FOREIGN KEY REFERENCES MAKE(IDMAKE),
	IMAGECAR VARCHAR(50)
)
--
CREATE TABLE BANK
(
	IDBANK INT IDENTITY(1,1) PRIMARY KEY,
	MAXAMOUNT FLOAT,
	INTERESTRATE FLOAT,
	LURATION NVARCHAR(50),
	MONEYTOPAY FLOAT,
	CONDITIONS NVARCHAR(100), 
)
--
CREATE TABLE COMPANY 
(
	IDCOMPANY INT IDENTITY(1,1) PRIMARY KEY,
	PHONE FLOAT,
	EMAIL NVARCHAR(30), 
)

